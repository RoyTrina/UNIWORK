package practice;

public class tet {
    public static int add(int i, int j) {
        return i + j;
    }

    public static int subtract(int i, int j) {
        return i - j;
    }

    public static int divide(int i, int j) {
        return i / j;
    }

    public static int prod(int i, int j) {
        return i * j;
    }

}
